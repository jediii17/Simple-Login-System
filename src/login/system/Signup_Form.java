    private void Browse_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Browse_ButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser browse = new JFileChooser();
        browse.setCurrentDirectory(new File (System.getProperty("user.home")));
        
        //File Extension
        FileNameExtensionFilter fileFilter = new FileNameExtensionFilter("IMAGE FILES (*.jpg;*.jpeg;*.png;*.gif)",
                "jpg","png","gif");//File Extension
        
        browse.addChoosableFileFilter(fileFilter);  
        
       int fileImage = browse.showSaveDialog(null);
        
       if(fileImage == 0)
          {
            //JOptionPane.showMessageDialog(null,"Image Accepted"); 
            File selectedFile = browse.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            condition_image = path;
            ImageIcon myImage = new ImageIcon(path);
            Image scaled_img = myImage.getImage().getScaledInstance(Photo_Display_Signup.getWidth(),
                    Photo_Display_Signup.getHeight(),Image.SCALE_SMOOTH);
            ImageIcon TrueImage = new ImageIcon(scaled_img);
            
            Photo_Display_Signup.setIcon(TrueImage);
           
          } 
       else
            {
           
                JOptionPane.showMessageDialog(null,"No Image Selected"); 
           
            }
        
       
        
    }//GEN-LAST:event_Browse_ButtonActionPerformed

        
    
