    private void Signup_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Signup_ButtonActionPerformed
        // TODO add your handling code here:
        
        String temp_password = String.valueOf(Password_Display_Signup.getPassword());
        String temp_retypepassword = String.valueOf(RetypePassword_Display_Signup.getPassword());
        
            if (Firstname_Display_Signup.getText().isEmpty() || Lastname_Display_Signup.getText().isEmpty()
                || Username_Display_Signup.getText().isEmpty() || temp_password.isEmpty()|| condition_image == null){
                JOptionPane.showMessageDialog(null, "Fill up All Fields"); 
               
            } else {
                if (!(temp_password.equals(temp_retypepassword))){
                 JOptionPane.showMessageDialog(null, "Password does not match");    
                }else{
                    
                   Connection conn = My_Connection.getConnection();
                   PreparedStatement ps;
                   
                    try {
                        ps = conn.prepareStatement("INSERT INTO `user_data`(`Firstname`, `Lastname`, `Username`, "
                                + "`Password`, `Picture`) VALUES (?,?,?,?,?)");
                        ps.setString(1, Firstname_Display_Signup.getText());
                        ps.setString(2, Lastname_Display_Signup.getText());
                        ps.setString(3, Username_Display_Signup.getText());
                        ps.setString(4, temp_password);
                        InputStream blob_img = new FileInputStream(new File(condition_image));
                        ps.setBlob(5, blob_img);
                        
                        if(ps.executeUpdate()==1){
                            JOptionPane.showMessageDialog(null,"Account Successfully Created"); 
                            this.dispose();
                            new Cruz_LoginSystem().setVisible(true);
                            
                        }else{
                           JOptionPane.showMessageDialog(null,"Account Creation Error"); 
                        }
                       
                        
                    } catch (Exception ex) {
                        Logger.getLogger(Signup_Form.class.getName()).log(Level.SEVERE, null, ex);
                    } 
                    
                    
                }
            }   
    }//GEN-LAST:event_Signup_ButtonActionPerformed

